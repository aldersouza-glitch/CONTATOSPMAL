const dataPath = path.join(process.cwd(), 'data.ts');
let content = fs.readFileSync(dataPath, 'utf8');

const match = content.match(/export const OFFICER_DATA: Officer\[\] = (\[[\s\S]*?\]);/);
if (!match) process.exit(1);

const officersRaw = match[1];
const lines = officersRaw.split('\n');
const existingOfficers = [];

for (let line of lines) {
    if (line.trim().startsWith('{')) {
        const getVal = (key) => {
            const m = line.match(new RegExp(key + ":\\s*'([^']*)'"));
            return m ? m[1] : '';
        };
        existingOfficers.push({
            matricula: getVal('matricula'),
            name: getVal('name').trim().toUpperCase()
        });
    }
}

const linesToCheck = userListRaw.split('\n').filter(l => l.trim() !== '');
const missing = [];

for (let line of linesToCheck) {
    // Regex to extract MAT and NAME
    // Example: CEL QOEM PM MAT. 75175 WALTER DO VALLE DE MELO JUNIOR 99377-5959
    const matMatch = line.match(/MAT\.\s*([\d\.]+|---)/);
    const matricula = matMatch ? matMatch[1].replace(/\.$/, '') : '';
    
    // Extract name: between rank/info and phone
    // We assume the phone is at the end
    const phoneMatch = line.match(/[\d\s-]{8,}$/);
    const contact = phoneMatch ? phoneMatch[0].trim() : '';
    
    // Name is what's left between MAT and Phone
    let namePart = line;
    if (matMatch) {
       namePart = line.substring(line.indexOf(matMatch[0]) + matMatch[0].length).trim();
    }
    if (contact) {
       namePart = namePart.substring(0, namePart.lastIndexOf(contact)).trim();
    }
    
    const rankMatch = line.match(/^(CEL|TC|MAJ|CAP|1º TEN|2º TEN|ASP|SUB|SGT)\s/i);
    const rank = rankMatch ? rankMatch[1].toUpperCase() : 'OFICIAL';
    
    const name = namePart.replace(/^\d\s/, '').trim().toUpperCase();

    if (!matricula && !name) continue;

    // Check if exists
    const exists = existingOfficers.some(o => 
        (matricula && matricula !== '---' && matricula !== '.' && o.matricula === matricula) ||
        (o.name === name)
    );

    if (!exists) {
        missing.push({
            id: 'out-' + (Math.random().toString(36).substring(2, 7)),
            category: 'Outros',
            unit: 'PMAL',
            rank: rank,
            matricula: matricula || '---',
            name: name,
            role: 'Oficial',
            contact: contact
        });
    }
}

console.log('Total entries provided:', linesToCheck.length);
console.log('Total missing entries:', missing.length);

if (missing.length > 0) {
    // Append missing to OFFICER_DATA
    let newDataStr = officersRaw.trim();
    // remove trailing ']'
    newDataStr = newDataStr.substring(0, newDataStr.lastIndexOf(']'));
    
    missing.forEach(m => {
        newDataStr += `  { id: '${m.id}', category: '${m.category}', unit: '${m.unit}', rank: '${m.rank}', matricula: '${m.matricula}', name: '${m.name}', role: '${m.role}', contact: '${m.contact}' },\n`;
    });
    newDataStr += ']';
    
    const newContent = content.replace(/export const OFFICER_DATA: Officer\[\] = \[[\s\S]*?\];/, `export const OFFICER_DATA: Officer[] = ${newDataStr};`);
    fs.writeFileSync(dataPath, newContent);
    console.log('Updated data.ts');
}
